#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	c = ((char)c);
	while ((i + 1) != 0)
	{
		if (s[i] == c && (c >= 0 && c <= 127))
			return ((char *)(s + i));
		i--;
	}
	if (s[i] == '\0' && c == '\0')
		return ((char *)(s + i));
	return (0);
}
